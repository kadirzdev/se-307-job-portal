namespace JobPortalSystem;

public class Resume
{
    
    // Resume class to represent a job seeker's resume
    
    private String name;
    private String address;
    private String phone;
    private String email;
    private String experience;
    
    public Resume(String name, String address, String phone, String email, String experience)
    {
        this.name = name;
        this.address = address;
        this.phone = phone;
        this.email = email;
        this.experience = experience;
    }
    
    
    
    
    
    
    






}