namespace JobPortalSystem;

public class Resume
{
    
    // Resume class to represent a job seeker's resume
    
    private String name;
    private String email;
    private String phone;
    
    // Constructor
    public Resume(String name, String email, String phone)
    {
        this.name = name;
        this.email = email;
        this.phone = phone;
    }


}